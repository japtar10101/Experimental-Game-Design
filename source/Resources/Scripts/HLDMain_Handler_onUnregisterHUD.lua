function HLDMain.onUnregisterHUD ( sInstanceID )
--------------------------------------------------------------------------------
	
    local t    = this.tHUDInstances ( )
	local iMax = table.getSize ( t )
    
    for i = 0, iMax - 1
    do
        if ( sInstanceID == table.getAt ( t, i ) )
        then
            table.removeAt ( t, i )
            break
        end
    end
	
--------------------------------------------------------------------------------
end
