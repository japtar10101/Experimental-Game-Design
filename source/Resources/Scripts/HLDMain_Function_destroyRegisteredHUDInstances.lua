function HLDMain.destroyRegisteredHUDInstances ( )
--------------------------------------------------------------------------------
	
    local t    = this.tHUDInstances ( )
	local iMax = table.getSize ( t )
    
    for i = 0, iMax - 1
    do
        hud.destroyTemplateInstance ( this.getUser ( ), table.getAt ( t, i ) )
    end
	
    table.empty ( t )
    
--------------------------------------------------------------------------------
end
