function HLDMessageBox.onDestroyHUD ( id )
--------------------------------------------------------------------------------
	
    -- Unregister HUD
    --
    user.sendEvent ( application.getCurrentUser ( ), "HLDMain", "onRegisterHUD", id )

    -- Destroy HUD
    --
	hud.destroyTemplateInstance ( application.getCurrentUser ( ), id )
    
--------------------------------------------------------------------------------
end
