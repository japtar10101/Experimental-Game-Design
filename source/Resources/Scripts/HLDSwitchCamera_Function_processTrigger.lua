function HLDSwitchCamera.processTrigger ( )
--------------------------------------------------------------------------------
	
    local privateVariables  = this.Private ( )
    local targetObject      = table.getAt ( privateVariables, 0 )
    if  ( targetObject )
    then
        application.setCurrentUserActiveCamera ( targetObject )
    end
	
--------------------------------------------------------------------------------
end
